#include<stdio.h>

///////////////////////////////////////////////////////////////////////////
//
// Function name : Number
// Description : It is used to accept number from user and classify it as small, medium or large.
// Input : Integer
// Output : Integer
// Author : Prasanna Shamsundar Landge
// Date : 26/10/2025
//
///////////////////////////////////////////////////////////////////////////

void Number(int iNo)
{
    if(iNo < 50)
    {
        printf("small\n");
    }
    else if(iNo >= 50 && iNo < 100)
    {
        printf("medium\n");
    }
    else
    {
        printf("large\n");
    }
}

//////////////////////////////////////////////////////////////////////////
//
// Entry point function
//
//////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter number: ");
    scanf("%d",&iValue);
    Number(iValue);
    return 0;
}

