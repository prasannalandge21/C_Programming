void PrintEven(int iNo)
{
    if(iNo <= 0)
    {
        return;
    }

    // Logic
    int i = 0;
    for(i = 1; i <= iNo; i++)
    {
        printf("%d ", i * 2);
    }
    printf("\n"); 
}

///////////////////////////////////////////////////////////////////////////
//
// Entry Point function for the Application
//
///////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number\n");
    scanf("%d",&iValue);
    PrintEven(iValue);

    return 0;
}

