#include<stdio.h>

///////////////////////////////////////////////////////////////////////////
//
// Function name : main
// Description : It is used to accept number from user and print numbers from 1 to that
// Input : Integer
// Output : Numbers
// Author : Prasanna Shamsundar Landge
// Date : 26/10/2025
//
///////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    for(int i = 1; i <= iNo; i++)
    {
        printf("%d\t", i);
    }
    printf("\n");
}

////////////////////////////////////////////////////////////////////////////
//
// Entry point function
//
////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    printf("Enter number: ");
    scanf("%d",&iValue);
    Display(iValue);
    return 0;
}

