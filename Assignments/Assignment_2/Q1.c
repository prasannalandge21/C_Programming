#include<stdio.h>

///////////////////////////////////////////////////////////////////////////
//
// Function name : Display
// Description : It is used to print * on screen
// Input : Void
// Output : void
// Author : Prasanna Shamsundar Landge
// Date : 21/10/2025
//
///////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt = 0;                               // Counter

    // Write Updater

    while( iCnt < iNo)                          // updater
    {
        printf (" * ");
        iCnt++;
    }
}

///////////////////////////////////////////////////////////////////////////
//
// Entry Point Function 
//
///////////////////////////////////////////////////////////////////////////


int main()
{
    int iValue = 0;

    printf("Enter number: ");
    scanf("%d" , &iValue);

    Display(iValue);

    return 0;
}

